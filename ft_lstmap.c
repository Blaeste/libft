#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_elem;
	void	*temp;

	if (!f && !del)
		return (NULL);
	new_list = NULL;
	while (lst)
	{
		temp = (*f)(lst->content);
		new_elem = ft_lstnew(temp);
		if (!new_elem)
		{
			free(temp);
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_elem);
		lst = lst->next;
	}
	return (new_list);
}
